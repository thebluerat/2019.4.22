package color0423;

public class Cal {

	public static void main(String[] args) {
		
		
System.out.println(덧셈(5, -3));
System.out.println(곱셈(3, 4));
System.out.println(뺄셈(10, 70));
System.out.println(나눗셈(7, 2));
System.out.println(나머지(7, 2));
	}
	
	
  static int 덧셈(int a, int b) {
	return a + b;
  }
	
	static int 곱셈(int a, int b) {
	return a * b;
	}
	
	static int 뺄셈(int a, int b) {
		return a - b;
	}
	
	static int 나눗셈(int a, int b) {
	return a / b;
	}
	
	static int 나머지(int a, int b) {
		return a % b;
	}
	
	
	

	
	
		
		
}
